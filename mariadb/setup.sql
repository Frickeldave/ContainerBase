USE mysql;
FLUSH PRIVILEGES;
ALTER USER root@localhost IDENTIFIED BY '#MDB_ROOTPWD#';
DELETE FROM mysql.user WHERE User='root' AND Host NOT IN ('localhost', '127.0.0.1', '::1');
DELETE FROM mysql.user WHERE User='';
DROP DATABASE IF EXISTS test;
DELETE FROM mysql.db WHERE Db='test' OR Db='test\_%';
CREATE USER '#MDB_ADMINUSER#'@'%' IDENTIFIED BY '#MDB_ADMINPWD#';
GRANT ALL PRIVILEGES ON *.* TO '#MDB_ADMINUSER#'@'%' WITH GRANT OPTION;
CREATE USER '#MDB_BACKUPUSER#'@'%' IDENTIFIED BY '#MDB_BACKUPPWD#';
GRANT SELECT, LOCK TABLES ON *.* TO '#MDB_BACKUPUSER#'@'%' WITH GRANT OPTION;
GRANT SELECT, LOCK TABLES, SHOW VIEW, EVENT, TRIGGER ON `myschema`.* TO '#MDB_BACKUPUSER#'@'%';
CREATE USER '#MDB_HEALTHUSER#'@'%' IDENTIFIED BY '#MDB_HEALTHPWD#';
GRANT USAGE ON *.* TO '#MDB_HEALTHUSER#'@'%';
FLUSH PRIVILEGES;